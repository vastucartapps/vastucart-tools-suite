// Combine all places
export const INDIAN_PLACES: Place[] = [
  ...METROS,
  ...MAJOR_CITIES,
  ...DISTRICT_HQ,
  ...TIER_2_3_CITIES,
  ...ADDITIONAL_CITIES,
];

// Search function for autocomplete
export function searchPlaces(query: string, limit: number = 10): Place[] {
  if (!query || query.length < 2) return [];

  const normalizedQuery = query.toLowerCase().trim();

  // Exact match first, then starts with, then includes
  const exactMatches: Place[] = [];
  const startsWithMatches: Place[] = [];
  const includesMatches: Place[] = [];

  for (const place of INDIAN_PLACES) {
    const name = place.name.toLowerCase();
    const state = place.state.toLowerCase();

    if (name === normalizedQuery || state === normalizedQuery) {
      exactMatches.push(place);
    } else if (name.startsWith(normalizedQuery) || state.startsWith(normalizedQuery)) {
      startsWithMatches.push(place);
    } else if (name.includes(normalizedQuery) || state.includes(normalizedQuery)) {
      includesMatches.push(place);
    }
  }

  return [...exactMatches, ...startsWithMatches, ...includesMatches].slice(0, limit);
}

// Get place by name
export function getPlaceByName(name: string): Place | undefined {
  return INDIAN_PLACES.find(
    p => p.name.toLowerCase() === name.toLowerCase()
  );
}

// Utility to get timezone offset
export function getTimezoneOffset(place: Place): number {
  return place.tz;
}
